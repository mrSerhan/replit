package Replit;

public class replit069_asel {

	public static void main(String[] args) {
		 
		for (int i = 7; i >= 1; i--) 
	        {
	            for (int j = 1; j <= i; j++)
	            {
	                System.out.print(j+" ");
	            }
	             
	            System.out.println();
	        }
	         
	        //Printing lower half of the pattern
	         
	        for (int i = 2; i <= 7; i++) 
	        {
	            for (int j = 1; j <= i; j++)
	            {
	                System.out.print(j+" ");
	            }
	             
	            System.out.println();
	        }

	}

}
